function [nabla_V] = ariane1_con(m)
k = [0.1101; 0.1532; 0.2154];
v_e = [2647.2; 2922.4; 4344.3];
M_f = [1700 + (1 + k(3))*m(3) + (1 + k(2))*m(2) + k(1)*m(1) ; 
        1700 + (1 + k(3))*m(3) + k(2)*m(2) ; 
        1700 + k(3)*m(3)]; 
M_i = M_f + m;
nabla_V = v_e(1) * log(M_i(1)/M_f(1)) + v_e(2) * log(M_i(2)/M_f(2)) + v_e(3) * log(M_i(3)/M_f(3)) - 11527; 
end

